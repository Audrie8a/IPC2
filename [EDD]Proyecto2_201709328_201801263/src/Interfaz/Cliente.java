    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String BuscarDpi = JOptionPane.showInputDialog("ESCRIBA EL NUMERO DE DPI");
        String ModificarNombre = JOptionPane.showInputDialog("ESCRIBA EL NOMBRE");
        String ModificarApellido = JOptionPane.showInputDialog("ESCRIBA EL APELLIDO ");
        String ModificarGenero = JOptionPane.showInputDialog("ESCRIBA EL GENERO ");
        String ModificarFecha = JOptionPane.showInputDialog("ESCRIBA LA FECHA ");
        String AuxTelefono = JOptionPane.showInputDialog("ESCRIBA EL TELEFONO ");
        int ModificarTelefono = Integer.parseInt(AuxTelefono);
        String ModificarDireccion= JOptionPane.showInputDialog("ESCRIBA LA DIRECCION");

        nodoCliente BuscarTemporal;
        BuscarTemporal=tabl.extraerNodo(BuscarDpi);
        if (BuscarTemporal!=null && BuscarTemporal.getDPI().equals(BuscarDpi)) {
            BuscarTemporal.setNombre(ModificarNombre);
            BuscarTemporal.setApellidos(ModificarApellido);
            BuscarTemporal.setGenero(ModificarGenero);
            BuscarTemporal.setFecha(ModificarFecha);
            BuscarTemporal.setTelefono(ModificarTelefono);
            BuscarTemporal.setDireccion(ModificarDireccion);

        }else if (BuscarTemporal!=null && BuscarTemporal.getDPI().equals(BuscarDpi)==false) {
            nodoClienteLista Temporal=BuscarTemporal.getListaDeCliente().DevolverNodo(BuscarDpi);
            Temporal.setNombre(ModificarNombre);
            Temporal.setApellidos(ModificarApellido);
            Temporal.setGenero(ModificarGenero);
            Temporal.setFecha(ModificarFecha);
            Temporal.setTelefono(ModificarTelefono);
            Temporal.setDireccion(ModificarDireccion);
        }else{
            JOptionPane.showMessageDialog(null,"NO SE ENCONTRO   "+BuscarDpi,"RAYOS",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

